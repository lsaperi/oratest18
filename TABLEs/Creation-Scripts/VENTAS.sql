CREATE TABLE VENTAS
   (	ID NUMBER,
	FECHA DATE,
	MONTO NUMBER
   )
  PARTITION BY RANGE (FECHA)
 (PARTITION VENTAS_2022  VALUES LESS THAN (TO_DATE(' 2023-01-01 00:00:00', 'SYYYY-MM-DD HH24:MI:SS', 'NLS_CALENDAR=GREGORIAN')) ,
 PARTITION VENTAS_2023  VALUES LESS THAN (TO_DATE(' 2024-01-01 00:00:00', 'SYYYY-MM-DD HH24:MI:SS', 'NLS_CALENDAR=GREGORIAN')) ) ;

